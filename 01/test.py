import subprocess

def check(expr, result):
    out = subprocess.run(['./a.out', expr], stdout = subprocess.PIPE)
    if out.stdout.decode('ascii') != result:
        print('error {0} != {1}, got {2}'.format(expr, result, out.stdout))
    else:
        print('OK')


check('2', '2')
